public class operators{
    public static void main(String args[]){
        System.out.println("And of 5 and 6 is -> " + (5 & 6));
        System.out.println("Or of 5 and 6 is -> " + (5 | 6));
        System.out.println("Xor of 5 and 6 is -> " + (5 ^ 6));
        System.out.println("One's Compliment of 5 is -> " + (~5)); 
        System.out.println("Left Shift -> " + (5 << 2));
        System.out.println("Right Shift -> " + (6 >> 1));
    }
}